    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Transaksi");
        model.addColumn("Merk Frame");
        model.addColumn("Jenis Lensa Kanan");
        model.addColumn("Ukuran Lensa Kanan");
        model.addColumn("Jenis Lensa Kiri");
        model.addColumn("Ukuran Lensa Kiri");
        model.addColumn("Nama Pelanggan");
        model.addColumn("Nama User");
        model.addColumn("Total");
        model.addColumn("Tanggal");

        try {
            String sql = "Select * from view_transaksi where kode_transaksi='" + txtCari.getText() + "' or nama_pelanggan='" + txtCari.getText() + "' or DATE_FORMAT(tanggal,'%Y-%m') ='" + txtCari.getText() + "' or DATE_FORMAT(tanggal,'%m') ='" + txtCari.getText() + "' or DATE_FORMAT(tanggal,'%Y-%m%-%d') ='" + txtCari.getText() + "' or DATE_FORMAT(tanggal,'%Y') ='" + txtCari.getText() + "'";
            java.sql.Connection conn = (Connection) Konfig.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet res = pst.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5),
                    res.getString(6),
                    res.getString(7),
                    res.getString(8),
                    res.getString(9),
                    res.getString(10)
                });
            }
            jTable1.setModel(model);
        }
        catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }
        txtCari.requestFocus();

    }//GEN-LAST:event_jButton7ActionPerformed

